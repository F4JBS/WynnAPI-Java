public class PrintAPI {

    /**
     * @param printing Input to Automatically System.out.print()
     */
    public static void print(String printing){
        System.out.print(printing);
    }

    /**
     * @param printing Input to Automatically System.out.println()
     */
    public static void println(String printing){
        System.out.println(printing);
    }

}
